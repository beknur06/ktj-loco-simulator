package kz.ktj.digitaltwin.simulator.model;

/**
 * Фазы движения локомотива.
 * Каждая фаза влияет на поведение параметров:
 * - при разгоне растёт ток, температуры, расход
 * - на крейсере всё стабильно
 * - при торможении падает скорость, растёт давление в ТЦ
 */
public enum DrivingPhase {
    STARTUP,        // запуск систем, прогрев
    ACCELERATING,   // разгон
    CRUISING,       // крейсерский ход
    COASTING,       // выбег (без тяги)
    BRAKING,        // торможение
    STOPPED         // стоянка (станция, светофор)
}
